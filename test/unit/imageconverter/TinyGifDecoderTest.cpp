// Simple Print implementation to capture BMP output
class TestOutput : public Print {
 public:
  virtual size_t write(uint8_t c) override {
    data.push_back(c);
    return 1;
  }
  std::vector<uint8_t> data;
};

int main() {
  TestUtils::TestRunner runner("TinyGifDecoder");

  // Test: Decode test GIF to BMP
  Serial.println("[TinyGIF] Testing decoder with homer_tiny.gif (64x64)...");
  TestOutput output;
  bool decodeResult = TinyGifDecoder::decodeGifToBmp(testGif, sizeof(testGif), output);

  runner.expectTrue("GIF decoding should succeed", decodeResult);

  if (decodeResult) {
    Serial.printf("[TinyGIF] Generated %d bytes of BMP data\n", output.data.size());

    // Validate BMP header
    bool validHeader = output.data.size() >= 54 && output.data[0] == 'B' && output.data[1] == 'M';
    runner.expectTrue("BMP header should be valid", validHeader);

    if (validHeader) {
      // Extract dimensions from BMP header
      int width = output.data[18] | (output.data[19] << 8) | (output.data[20] << 16) | (output.data[21] << 24);
      int height = output.data[22] | (output.data[23] << 8) | (output.data[24] << 16) | (output.data[25] << 24);

      runner.expectEqual("Width should be 64", width, 64);
      runner.expectEqual("Height should be 64", height, 64);

      // Verify output size includes row padding
      int rowBytes = 64 * 3;                   // 192 bytes per row
      int padding = (4 - (rowBytes % 4)) % 4;  // 0 padding (192 % 4 == 0)
      int expectedSize = 54 + (rowBytes + padding) * 64;
      runner.expectEqual("Output size should match", (int)output.data.size(), expectedSize);
    }
  }

  return runner.getResult();
}